package com.demo.code;

public class CityByCode {
	public String getCityByCode(int number) {
		if(number==1) {
			return "DELHI";
		}
		else if(number==2) {
			return "NOIDA";
		}
		else if(number==2) {
			return "GUARGAON";
		}
		else if(number==2) {
			return "BANGLORE";
		}
		else {
			return "INVALIDCODE";
		}

	}

}
